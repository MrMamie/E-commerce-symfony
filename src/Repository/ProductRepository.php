/**
 * @method Product|null find($id, $lockMode = null, $lockVersion = null)
 * @method Product|null findOneBy(array $criteria, array $orderBy = null)
 * @method Product[]    findAll()
 * @method Product[]    findBy(array $criteria, array $orderBy = null, $limit = null, $offset = null)
 */
class ProductRepository extends ServiceEntityRepository
{
    public function __construct(ManagerRegistry $registry)
    {
        parent::__construct($registry, Product::class);
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function add(Product $entity, bool $flush = true): void
    {
        $this->_em->persist($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

    /**
     * @throws ORMException
     * @throws OptimisticLockException
     */
    public function remove(Product $entity, bool $flush = true): void
    {
        $this->_em->remove($entity);
        if ($flush) {
            $this->_em->flush();
        }
    }

        
    /**
     * findWithSearch
     *
     * requête qui permet de récupérer les produit en fonction de la recherche du user 
     * @return Product[]
     */
    public function findWithSearch(Search $search){
        
        $query = $this->createQueryBuilder('p')
        ->select() // p alias produit & c alias catégory
        ->join('p.category','c'); //jointure entre la table produit et category
        
        if(!empty($search->categories)){
            $query = $query->andWhere('c.id IN (:categories)') // :categories passage de parametre
            ->setParameter('categories',$search->categories); // à quoi correspond le parametre :catégories
            
        }
        if (!empty($search->string)) {
            $query = $query
                ->andWhere('p.name LIKE :string')
                ->setParameter('string', "%{$search->string}%");
            }
            return $query->getQuery()->getResult(); // retourne le resultat
}
